class آلة_افتراضية:
    def __init__(self):
        self.سجلات = {f"R{i}": 0 for i in range(16)}
        self.ذاكرة = bytearray(4096)
        self.مكدس = []
        self.مؤشر = 0
        self.برنامج = []
        self.تسميات = {}
        self.أعلام = {"صفر": False, "سالب": False, "محمل": False}
        self.قيد_التشغيل = False
    
    def حمل(self, برنامج):
        self.برنامج = برنامج
        self.مؤشر = 0
        self.تسميات = {}
        for i, تعليمة in enumerate(برنامج):
            if تعليمة[0] == "LABEL":
                self.تسميات[تعليمة[1]] = i
    
    def نفذ(self):
        self.قيد_التشغيل = True
        while self.قيد_التشغيل and self.مؤشر < len(self.برنامج):
            ت = self.برنامج[self.مؤشر]
            عملية = ت[0]
            
            if عملية == "LOAD":
                self.سجلات["R0"] = ت[1]
            elif عملية == "ADD":
                self.سجلات["R0"] += ت[1]
            elif عملية == "SUB":
                self.سجلات["R0"] -= ت[1]
            elif عملية == "MUL":
                self.سجلات["R0"] *= ت[1]
            elif عملية == "DIV":
                if ت[1] != 0: self.سجلات["R0"] //= ت[1]
            elif عملية == "MOD":
                if ت[1] != 0: self.سجلات["R0"] %= ت[1]
            elif عملية == "POW":
                self.سجلات["R0"] **= ت[1]
            elif عملية == "SQRT":
                self.سجلات["R0"] = int(self.سجلات["R0"] ** 0.5)
            elif عملية == "CMP":
                self.أعلام["صفر"] = (self.سجلات["R0"] == ت[1])
                self.أعلام["سالب"] = (self.سجلات["R0"] < ت[1])
            elif عملية == "JMP":
                self.مؤشر = self.تسميات[ت[1]]
                continue
            elif عملية == "JZ":
                if self.أعلام["صفر"]:
                    self.مؤشر = self.تسميات[ت[1]]
                    continue
            elif عملية == "JNZ":
                if not self.أعلام["صفر"]:
                    self.مؤشر = self.تسميات[ت[1]]
                    continue
            elif عملية == "PUSH":
                self.مكدس.append(self.سجلات["R0"])
            elif عملية == "POP":
                if self.مكدس: self.سجلات["R0"] = self.مكدس.pop()
            elif عملية == "PRINT":
                print(f"  🖨️  مخرجات: {self.سجلات['R0']}")
            elif عملية == "HALT":
                self.قيد_التشغيل = False
                return self.سجلات["R0"]
            
            self.مؤشر += 1
        return self.سجلات["R0"]

