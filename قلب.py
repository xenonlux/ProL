import sys
import os

# ========== قاموس الأوامر ==========
قاموس_الأوامر = {
    "شغل": "تشغيل", "شغّل": "تشغيل", "شغله": "تشغيل",
    "درب": "تدريب", "درّب": "تدريب", "دربه": "تدريب",
    "اختبر": "اختبار", "اختبره": "اختبار",
    "حمّل": "تحميل", "حمِّل": "تحميل",
    "احفظ": "حفظ", "احفظه": "حفظ",
    "ابنِ": "بناء", "ابني": "بناء", "أنشئ": "إنشاء", "أنشي": "إنشاء",
    "استعلم": "استعلام", "اعرض": "عرض", "ارسم": "رسم",
}

قاموس_الأهداف = {
    "نموذج": "نموذج", "النموذج": "نموذج", "نموذجي": "نموذج",
    "بيانات": "بيانات", "البيانات": "بيانات", "بياناتي": "بيانات",
    "شبكة": "شبكة", "الشبكة": "شبكة", "شبكتي": "شبكة",
    "طبقات": "طبقات", "الطبقات": "طبقات",
    "رسم": "رسم", "الرسم": "رسم", "مخطط": "رسم",
}

# ========== الذاكرة (السياق) ==========
ذاكرة = {
    "آخر_أمر": None,
    "آخر_هدف": None,
    "آخر_مصدر": None,
    "آخر_عدد": None,
    "التاريخ": []
}

# ========== تصحيح تلقائي ==========
تصحيحات = {
    "شغلق": "شغّل",
    "شغله": "شغّل",
    "دربق": "درّب",
    "دربه": "درّب",
    "ابني": "ابنِ",
    "ابنيق": "ابنِ",
    "حمله": "حمّل",
    "اختبره": "اختبر",
    "نموزج": "نموذج",
    "بيانات": "بيانات",
    "شبكه": "شبكة",
}

def صحح_الكلمة(كلمة):
    """يقترح تصحيحًا للكلمة الخاطئة"""
    if كلمة in تصحيحات:
        الصحيح = تصحيحات[كلمة]
        print(f"  💡 تصحيح تلقائي: '{كلمة}' ➜ '{الصحيح}'")
        return الصحيح
    return كلمة

# ========== تحليل متقدم ==========
def حلل_الجملة(نص):
    الكلمات = نص.strip().split()
    
    الأمر = None
    الهدف = None
    المصدر = None
    العدد = None
    
    for كلمة in الكلمات:
        كلمة_أصلية = كلمة
        كلمة = صحح_الكلمة(كلمة)
        
        if كلمة in قاموس_الأوامر:
            الأمر = قاموس_الأوامر[كلمة]
        elif كلمة in قاموس_الأهداف:
            if الهدف is None:
                الهدف = قاموس_الأهداف[كلمة]
            else:
                المصدر = قاموس_الأهداف[كلمة]
        elif كلمة.isdigit():
            العدد = int(كلمة)
        elif الهدف is not None and المصدر is None:
            المصدر = كلمة
    
    # استخدام الذاكرة للضمائر المفقودة
    if الهدف is None and ذاكرة["آخر_هدف"]:
        الهدف = ذاكرة["آخر_هدف"]
        print(f"  🧠 تذكرت الهدف: {الهدف}")
    if المصدر is None and ذاكرة["آخر_مصدر"] and الأمر in ["تشغيل", "تدريب"]:
        المصدر = ذاكرة["آخر_مصدر"]
        print(f"  🧠 تذكرت المصدر: {المصدر}")
    
    return {
        "الأمر": الأمر,
        "الهدف": الهدف,
        "المصدر": المصدر,
        "العدد": العدد,
        "النص_الأصلي": نص.strip()
    }

def صغ_بسيط(هيكل):
    أجزاء = []
    if هيكل["الأمر"]: أجزاء.append(f"الأمر = {هيكل['الأمر']}")
    if هيكل["الهدف"]: أجزاء.append(f"الهدف = {هيكل['الهدف']}")
    if هيكل["المصدر"]: أجزاء.append(f"المصدر = {هيكل['المصدر']}")
    if هيكل["العدد"] is not None: أجزاء.append(f"العدد = {هيكل['العدد']}")
    return " | ".join(أجزاء) if أجزاء else "لم أفهم شيئًا"

def صغ_تفصيلي(هيكل):
    if not هيكل["الأمر"]:
        return "عذرًا، لم أستطع فهم الأمر المطلوب."
    
    جملة = "فهمت أنك تريد: "
    
    if هيكل["الأمر"] == "تدريب":
        جملة += "تدريب"
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
        if هيكل["المصدر"]: جملة += f" على {هيكل['المصدر']}"
        if هيكل["العدد"]: جملة += f" لمدة {هيكل['العدد']} دورة"
    elif هيكل["الأمر"] == "تشغيل":
        جملة += "تشغيل"
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
        if هيكل["المصدر"]: جملة += f" على {هيكل['المصدر']}"
    elif هيكل["الأمر"] == "اختبار":
        جملة += "اختبار"
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
        if هيكل["المصدر"]: جملة += f" باستخدام {هيكل['المصدر']}"
    elif هيكل["الأمر"] in ["بناء", "إنشاء"]:
        جملة += هيكل["الأمر"]
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
        if هيكل["العدد"]: جملة += f" بـ {هيكل['العدد']} طبقة"
    elif هيكل["الأمر"] == "حفظ":
        جملة += "حفظ"
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
        if هيكل["المصدر"]: جملة += f" في {هيكل['المصدر']}"
    elif هيكل["الأمر"] in ["عرض", "رسم", "استعلام"]:
        جملة += f"{هيكل['الأمر']}"
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
    else:
        جملة += هيكل["الأمر"]
        if هيكل["الهدف"]: جملة += f" {هيكل['الهدف']}"
    
    return جملة + "."

def اطلب_التأكيد(هيكل):
    print("\n" + "="*50)
    print("تحليل بسيط:", صغ_بسيط(هيكل))
    print("تحليل تفصيلي:", صغ_تفصيلي(هيكل))
    print("="*50)
    
    while True:
        رد = input("\nهل هذا صحيح؟ (نعم/لا/خروج): ").strip().lower()
        if رد in ["نعم", "yes", "y", "صحيح", "صح"]:
            return True
        elif رد in ["لا", "no", "n", "خطأ", "غلط"]:
            return False
        elif رد in ["خروج", "exit", "q", "quit"]:
            print("إلى اللقاء.")
            sys.exit(0)
        else:
            print("الرجاء الإجابة بـ: نعم أو لا أو خروج")

def نفذ_الأمر(هيكل):
    if not هيكل["الأمر"]:
        print("لا يوجد أمر قابل للتنفيذ.")
        return None
    
    تعليمات = []
    
    if هيكل["الأمر"] == "تدريب":
        if هيكل["العدد"]: تعليمات.append(f"LOAD {هيكل['العدد']}")
        تعليمات.append(f"TRAIN {هيكل['الهدف'] or 'نموذج'}")
        if هيكل["المصدر"]: تعليمات.append(f"DATA {هيكل['المصدر']}")
    elif هيكل["الأمر"] == "تشغيل":
        تعليمات.append(f"RUN {هيكل['الهدف'] or 'نموذج'}")
        if هيكل["المصدر"]: تعليمات.append(f"DATA {هيكل['المصدر']}")
    elif هيكل["الأمر"] in ["بناء", "إنشاء"]:
        if هيكل["العدد"]: تعليمات.append(f"LOAD {هيكل['العدد']}")
        تعليمات.append(f"BUILD {هيكل['الهدف'] or 'شبكة'}")
    elif هيكل["الأمر"] == "حفظ":
        تعليمات.append(f"SAVE {هيكل['الهدف'] or 'نموذج'}")
        if هيكل["المصدر"]: تعليمات.append(f"TO {هيكل['المصدر']}")
    elif هيكل["الأمر"] in ["عرض", "استعلام"]:
        تعليمات.append(f"SHOW {هيكل['الهدف'] or 'الكل'}")
    elif هيكل["الأمر"] == "رسم":
        تعليمات.append(f"PLOT {هيكل['الهدف'] or 'رسم'}")
    else:
        تعليمات.append(f"{هيكل['الأمر'].upper()} {هيكل['الهدف'] or ''}")
    
    print("\n📦 تعليمات VM المولَّدة:")
    for ت in تعليمات:
        print(f"   ➜ {ت}")
    
    # تحديث الذاكرة
    ذاكرة["آخر_أمر"] = هيكل["الأمر"]
    ذاكرة["آخر_هدف"] = هيكل["الهدف"]
    ذاكرة["آخر_مصدر"] = هيكل["المصدر"]
    ذاكرة["آخر_عدد"] = هيكل["العدد"]
    ذاكرة["التاريخ"].append(هيكل["النص_الأصلي"])
    
    return تعليمات

def حوار_تفاعلي():
    print("="*60)
    print("  🫀  مرحبًا بك في القلب الناطق  🫀")
    print("  اكتب جملتك العربية الحرة")
    print("  أوامر: 'ذاكرة' للسياق | 'تاريخ' للسجل | 'خروج'")
    print("="*60)
    
    while True:
        try:
            جملة = input("\n📝 جملتك > ").strip()
        except (EOFError, KeyboardInterrupt):
            print("\nإلى اللقاء.")
            break
        
        if not جملة:
            continue
        
        if جملة.lower() in ["خروج", "exit", "q", "quit"]:
            print("إلى اللقاء.")
            break
        
        if جملة == "ذاكرة":
            print("\n🧠 الذاكرة الحالية:")
            for مفتاح, قيمة in ذاكرة.items():
                if مفتاح != "التاريخ":
                    print(f"   {مفتاح}: {قيمة}")
            continue
        
        if جملة == "تاريخ":
            print("\n📜 سجل الأوامر:")
            for i, س in enumerate(ذاكرة["التاريخ"], 1):
                print(f"   {i}. {س}")
            continue
        
        هيكل = حلل_الجملة(جملة)
        
        if اطلب_التأكيد(هيكل):
            نفذ_الأمر(هيكل)
            print("✅ تم التنفيذ.")
        else:
            print("🔄 حسنًا، أعد كتابة الجملة من فضلك.")

def حلل_ملف(مسار):
    if not os.path.exists(مسار):
        print(f"❌ الملف غير موجود: {مسار}")
        return
    
    with open(مسار, 'r', encoding='utf-8') as ملف:
        for رقم, سطر in enumerate(ملف, 1):
            سطر = سطر.strip()
            if not سطر or سطر.startswith('#'):
                continue
            
            print(f"\n--- سطر {رقم}: {سطر} ---")
            هيكل = حلل_الجملة(سطر)
            
            if اطلب_التأكيد(هيكل):
                نفذ_الأمر(هيكل)
            else:
                print("⏭️  تخطي هذا السطر.")

if __name__ == "__main__":
    if len(sys.argv) > 1:
        حلل_ملف(sys.argv[1])
    else:
        حوار_تفاعلي()
