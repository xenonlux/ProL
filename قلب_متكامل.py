# ========== VM كاملة ==========
class VM:
    def __init__(self):
        self.registers = {"RAX": 0, "RBX": 0, "RCX": 0, "RDX": 0}
        self.memory = bytearray(1024)
        self.ip = 0
        self.program = []
        self.labels = {}
        self.flags = {"ZF": 0, "CF": 0}
        
    def load(self, program):
        self.program = program
        self.ip = 0
        # مسح التسميات أولاً
        for i, inst in enumerate(program):
            if inst[0] == "LABEL":
                self.labels[inst[1]] = i
                
    def run(self):
        while self.ip < len(self.program):
            inst = self.program[self.ip]
            op = inst[0]
            
            if op == "LOAD":
                self.registers["RAX"] = inst[1]
            elif op == "ADD":
                self.registers["RAX"] += inst[1]
            elif op == "SUB":
                self.registers["RAX"] -= inst[1]
            elif op == "MUL":
                self.registers["RAX"] *= inst[1]
            elif op == "DIV":
                if inst[1] != 0:
                    self.registers["RAX"] //= inst[1]
            elif op == "MOV":
                self.registers[inst[1]] = inst[2]
            elif op == "CMP":
                self.flags["ZF"] = 1 if self.registers["RAX"] == inst[1] else 0
                self.flags["CF"] = 1 if self.registers["RAX"] < inst[1] else 0
            elif op == "JMP":
                self.ip = self.labels[inst[1]]
                continue
            elif op == "JZ":
                if self.flags["ZF"] == 1:
                    self.ip = self.labels[inst[1]]
                    continue
            elif op == "HALT":
                return self.registers["RAX"]
            elif op == "PRINT":
                print(f"  🖨️  المخرجات: {self.registers['RAX']}")
            elif op == "STORE":
                addr = inst[1]
                if addr < len(self.memory):
                    self.memory[addr] = self.registers["RAX"] & 0xFF
            elif op == "INC":
                self.registers["RAX"] += 1
            elif op == "DEC":
                self.registers["RAX"] -= 1
            
            self.ip += 1
        return self.registers["RAX"]

